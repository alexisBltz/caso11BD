package org.app.controllers;

public class EstadoRegistro {
    private char estRegCod;
    private String estRegNom;
    private char estRegEstTra;

    public char getEstRegCod() {
        return estRegCod;
    }

    public void setEstRegCod(char estRegCod) {
        this.estRegCod = estRegCod;
    }

    public String getEstRegNom() {
        return estRegNom;
    }

    public void setEstRegNom(String estRegNom) {
        this.estRegNom = estRegNom;
    }

    public char getEstRegEstTra() {
        return estRegEstTra;
    }

    public void setEstRegEstTra(char estRegEstTra) {
        this.estRegEstTra = estRegEstTra;
    }
}
