package org.app.controllers;

public class CentroCosto {
    private char codCenCos;
    private String cenCosNombre;
    private char cenCostEstReg;

    public char getCodCenCos() {
        return codCenCos;
    }

    public void setCodCenCos(char codCenCos) {
        this.codCenCos = codCenCos;
    }

    public String getCenCosNombre() {
        return cenCosNombre;
    }

    public void setCenCosNombre(String cenCosNombre) {
        this.cenCosNombre = cenCosNombre;
    }

    public char getCenCostEstReg() {
        return cenCostEstReg;
    }

    public void setCenCostEstReg(char cenCostEstReg) {
        this.cenCostEstReg = cenCostEstReg;
    }
}
