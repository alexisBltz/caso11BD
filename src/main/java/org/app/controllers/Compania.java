package org.app.controllers;

public class Compania {
    private int comCod;
    private String comNom;
    private int comEmprCod;
    private String comDir;
    private char comEstRegComCod;

    public int getComCod() {
        return comCod;
    }

    public void setComCod(int comCod) {
        this.comCod = comCod;
    }

    public String getComNom() {
        return comNom;
    }

    public void setComNom(String comNom) {
        this.comNom = comNom;
    }

    public int getComEmprCod() {
        return comEmprCod;
    }

    public void setComEmprCod(int comEmprCod) {
        this.comEmprCod = comEmprCod;
    }

    public String getComDir() {
        return comDir;
    }

    public void setComDir(String comDir) {
        this.comDir = comDir;
    }

    public char getComEstRegComCod() {
        return comEstRegComCod;
    }

    public void setComEstRegComCod(char comEstRegComCod) {
        this.comEstRegComCod = comEstRegComCod;
    }
}
